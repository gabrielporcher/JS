// function declaration (forma tradicional) [Dessa maneira, a função pode ser instanciada até mesmo antes dela ter sido declarada]
function soma(x, y){
    return x + y
}

// function expression (Atribui uma função anonima a uma variavel)
const sub = function (x, y){
    return x + y
}

//named function expression (mistura dos 2 primeiros tipos) [pouco usada]
const mult = function mult (x, y){
    return x + y
}

